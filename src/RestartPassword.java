    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //botón para copiar al portapapeles
        String textToCopy = txtNewPassword.getText().trim();
        if (!textToCopy.isEmpty()) {
            // Copiar al portapapeles
            Toolkit.getDefaultToolkit()
                    .getSystemClipboard()
                    .setContents(new StringSelection(textToCopy), null);
            JOptionPane.showMessageDialog(this, "Contraseña copiada al portapapeles.",
                    "Éxito", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No hay texto para copiar.",
                    "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


    private void comboCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboCategoryActionPerformed
        // acción al seleccionar categoría
        // Manejar el evento de selección en comboCategory
        String selectedCategory = (String) comboCategory.getSelectedItem();
        if (selectedCategory != null && !selectedCategory.equals("Seleccionar")) {
            loadCategoryDetails(selectedCategory);
        } else {
            txtCategoryID.setText("");
            txtCurrentName.setText("");
        }
    }//GEN-LAST:event_comboCategoryActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //botón para guardar

        String newName = txtNewName.getText().trim();
        String categoryID = txtCategoryID.getText().trim();
        
        if (categoryID.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Seleccione una categoría para modificar.", "Error", 
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (newName.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el nuevo nombre para la categoría seleccionada", "Error", 
                    JOptionPane.ERROR_MESSAGE);
            return;
        }


        // Actualizar el nombre de la categoría en la base de datos
        String query = "UPDATE productCategories SET categoryName = ? WHERE category_pk = ?";
        try (Connection con = ConnectionProvider.getCon(); PreparedStatement pst = con.prepareStatement(query)) {
            pst.setString(1, newName);  // Establecer el nuevo nombre
            pst.setLong(2, Long.parseLong(categoryID));  // Establecer el ID de la categoría

            int result = pst.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(null, "Categoría actualizada correctamente.");
                loadCategories();  // Recargar las categorías en el comboBox
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo actualizar la categoría. Intente nuevamente.", "Error", 
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al actualizar la categoría: " + e.getMessage(), "Error", 
                    JOptionPane.ERROR_MESSAGE);
        }
        txtNewName.setText("");
    }//GEN-LAST:event_btnSaveActionPerformed

